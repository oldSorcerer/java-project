#!/usr/bin/env bash
# Скрипт компиляции и запуска программы с нужными флагами JVM.

set -e

# Компилируем Java-код
javac Solution.java

# Запускаем с требуемыми параметрами JVM:
# -XX:+UseG1GC — выбор сборщика мусора G1 GC
# -Xmx1g — ограничение максимального размера кучи до 1 ГиБ
# -XX:MaxGCPauseMillis=150 — целевая максимальная пауза сборщика мусора 150 мс