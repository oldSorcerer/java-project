package ru.javarush.java.core.level02.task11;

public class Solution {
    public static void main(String[] args) {
        // Объявляем строковую переменную, содержащую год выпуска фильма
        String yearStr = "1999";

        // Преобразуем строку в числовой тип int
        int year = Integer.parseInt(yearStr);

        // Выводим числовое значение года на экран
        System.out.println(year);

    }
}