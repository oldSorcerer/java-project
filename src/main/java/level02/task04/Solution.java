package ru.javarush.java.core.level02.task04;

public class Solution {
    public static void main(String[] args) {
        // Объявляем и инициализируем уровень жизни героя
        int heroHealth = 10;

        // Увеличиваем значение на 1 (инкремент) и выводим результат
        System.out.println(++heroHealth);

        // Уменьшаем значение на 1 (декремент) и снова выводим результат
        System.out.println(--heroHealth);
    }
}